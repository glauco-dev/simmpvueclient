            <h2>História do Sindicato</h2>
            <div>
                <p>O SIMMP foi fundado, enquanto sindicato, em 20 de outubro de 1989, tendo como primeira presidente a
                    professora Luzinete Borges Barreto. Na época, a entidade contava com a participação de mais de cem
                    professores municipais, que já faziam parte da Associação do Magistério Municipal de Vitória da
                    Conquista (AMM/VC), fundada em 4 de setembro de 1987. Esta foi criada no momento em os servidores
                    públicos eram impedidos por lei de se organizarem em sindicatos. Entretanto, essa lei perdurou até 1988,
                    quando foi promulgada a nova Constituição.</p>
                <p>Em 1990, a categoria enfrentou o seu primeiro grande movimento enquanto sindicato: uma greve de 55 dias,
                    onde os docentes tiveram seus salários cortados. Esta foi considerada inconstitucional pela prefeitura,
                    resultando em um dissídio coletivo, cujo julgamento foi realizado em Salvador. No mesmo ano, a entidade
                    também realizou a primeira eleição da Diretoria Executiva e do Conselho Fiscal, conforme estabelecia o
                    estatuto.</p>
                <p>A década de noventa foi bastante intensa, tendo em vista a quantidade de greves e paralisações
                    realizadas.&nbsp; Este foi um período difícil, mas de grande mobilização dos professores que,
                    incansavelmente, denunciavam os abusos cometidos pelo poder público. Atraso e rebaixamento de salário,
                    estrutura precária das escolas, problemas com a merenda e o transporte escolar eram algumas das questões
                    que evidenciavam o desinteresse do governo pela qualidade da educação.</p>
                <p>Nesse período, importantes embates foram travados com as sucessivas administrações, como o cumprimento da
                    Lei 394 de dezembro de 1986, que garantia a eleição direta dos diretores e vice-diretores escolares,
                    cujo resultado foi positivo. A insatisfação com a administração municipal, entre 1992 e 1996, também
                    provocou grande mobilização da categoria, fato que foi determinante para a derrota do candidato da
                    situação.</p>
                <p>A medida que os anos foram se passando, o grupo tornou-se mais amplo. Fortes campanhas de sindicalização
                    atraiam mais e mais professores que, a partir de 2000, continuou promovendo intensos movimentos. É claro
                    que, em muitos momentos, a desmotivação era grande, pois, os avanços eram pouco palpáveis. Ainda assim,
                    panfletagens, manifestações em bairros da cidade, reuniões com a comunidade escolar, ocupação de prédios
                    públicos, movimentos de rua, dentre tantas outras ações, continuaram a fazer parte da trajetória do
                    SIMMP. Como resultado desse trabalho, o grupo alcançou importantes objetivos, como a realização do
                    concurso público, tendo em vista que boa parte das vagas eram ocupadas por contratados; o curso de
                    licenciatura&nbsp; para os docentes da rede; o cumprimento parcial da lei 11.738/2008, do Piso Nacional
                    do Magistério, dentre outros.</p>
                <p>Em 2011, a categoria aguardava ansiosa a aprovação do Plano de Carreira, também prevista na Lei do Piso.
                    Entretanto, esta foi surpreendida com um documento que não contemplava em nada as suas reivindicações,
                    ao contrário, retirava importantes conquistas. A tabela salarial que possuía cinco níveis e cinco
                    classes, diferenciando os professores por formação e tempo de serviço, foi reduzida a apenas dois
                    níveis. Todas as discussões realizadas um ano antes pelos professores – cujo resultado foi incluído em
                    uma minuta, entregue ao executivo –f oi totalmente desconsiderada. O fato causou grande indignação,
                    resultando em uma greve de 35 dias.</p>
                <p>No dia 20 de julho, mesmo à revelia dos docentes, o projeto foi aprovado pela Câmara Municipal de
                    Vereadores. A sessão foi bastante polêmica, tendo em vista a manifestação realizada. Nela, os
                    professores apresentaram cartões vermelhos e viraram as costas aos parlamentares após a aprovação do
                    projeto. Em seguida, se dirigiram à porta da Câmara, onde expuseram à comunidade o autoritarismo dos
                    gestores.</p>
                <p>Atualmente, uma das mais importantes bandeiras de luta dos referidos profissionais é a reformulação do
                    Plano de Carreira. Em 2014, uma greve de 15 dias foi realizada, sendo marcada por constantes atos
                    públicos. Durante uma manifestação pelas ruas do Bairro Brasil, as Avenidas Brumado e Frei Benjamim
                    ficaram interditadas por trinta minutos; as Praças Barão do Rio Branco e Nove de Novembro inúmeras vezes
                    foram ocupadas pelos professores que, através de faixas, panfletos e carro de som denunciavam as
                    irregularidades cometidas pelo poder público municipal; os prédios públicos também foram palco de
                    manifestações, como a Feijoada da Educação, realizada na sede da PMVC. Essas e tantas outras
                    manifestações marcaram a biografia do SIMMP, que continuará buscando mecanismos que possibilite o avanço
                    e o alcance de novas conquistas.</p>
                <p><strong>Comemorações</strong></p>
                <p>As confraternizações também se tornaram marca registrada da Entidade, que proporciona importantes
                    momentos de descontração com os associados. O forró do professor é a mais tradicional festa, realizada
                    anualmente, no mês de junho, onde a categoria desfruta de boas atrações musicais, espaços ornamentados
                    e, claro, muita comida e bebidas típicas. O Dia do Trabalhador também se tornou outra data marcante, em
                    virtude de unificar protesto e confraternização em um único evento. Passeios ciclísticos, caminhadas,
                    atividades físicas são algumas das atividades propostas. Desde o ano de 2012, também passou a fazer
                    parte desse conjunto a comemoração de final de ano, quando a categoria se reúne em um clube para um café
                    da manhã, com direito a banho de piscina, atrações musicais e sorteio de brindes.</p>
                <p><strong>Estrutura física</strong></p>
                <p>Durante os primeiros anos, muitas dificuldades permeavam a organização das atividades, sobretudo por
                    questões financeiras. A pequena quantidade de filiados e, consequentemente, de arrecadação, tornava
                    difícil a aquisição dos bens necessários ao bom funcionamento do SIMMP, que, dentre outras coisas, não
                    possuía sede própria. Aos poucos, a estrutura foi sendo montada. Com muito trabalho, as gestões foram
                    estruturando a entidade, que adquiriu carros próprios, equipamentos de som e comunicação, mobílias e,
                    claro, a sede – localizada no bairro Alto Maron. A prefeitura doou o terreno, cuja construção teve
                    início em 2005, sendo parcialmente concluída em 2007. Entretanto, a inauguração oficial aconteceu no ano
                    de 2008, quando a categoria pôde celebrar essa que foi uma das mais importantes conquistas.</p>
                <p><strong>Novo Estatuto</strong></p>
                <p>Em novembro de 2013, outra importante atividade marcou a biografia do sindicato: um congresso com a
                    participação de quase todos os filiados, onde foi referendado o novo estatuto sindical. Um marco na
                    história da entidade que, após 24 anos, realizou a primeira atualização estatutária. Através dele, o
                    SIMMP deixou de ser Sindicato dos Professores, tornando-se Sindicato dos Profissionais da Educação. As
                    novas políticas públicas educacionais promoveram a inclusão de outros trabalhadores ao quadro de
                    profissionais da educação, tornando possível a unificação da luta. A estrutura do Congresso foi montada
                    no Centro de Aperfeiçoamento Profissional (CAP) da Universidade Estadual do Sudoeste da Bahia (UESB).
                </p>
                <p>Atualmente, cerca de mil e quatrocentos profissionais compõem o SIMMP e nós parabenizamos a cada um por
                    persistir incansavelmente na luta e colaborar para o desenvolvimento institucional. Isso se deve a
                    dedicação continuada e o amadurecimento da categoria que, mesmo diante de tantas adversidades, preservou
                    a combatividade dos primeiros anos.</p>
